X = int(input())
Y= int(input())

# quociente da divisão inteira
quociente_inteiro = X // Y
print(quociente_inteiro)

# resto a divisão inteira 
resto= X % Y
print(resto)
#quociente da divisão real
quociente_real = X / Y
print(quociente_real)

#Produto de x vezes Y
produto = X * Y
print(produto)

#Y elevado à terceira potência
potencia = Y ** 3
print(potencia)