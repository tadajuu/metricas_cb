X = int(input("Digite o valor de X:"))
Y = int(input("Digite o valor de Y:"))

# 1. Quociente da divisão inteira
quociente_inteiro = X // Y
# 2. Resto da divisão inteira
resto_inteiro = X % Y
# 3. Quociente da divisão real
quociente_real = X / Y
# 4. Produto de X vezes Y
produto = X * Y
# 5. Valor de Y elevado à tereira potência
potencia = Y ** 3

# 5. Impressão na ordem solicitada
print (quociente_inteiro)
print(resto_inteiro)
print(quociente_real)
print(produto)
print(potencia)