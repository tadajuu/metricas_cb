# Entrada
X = int(input("Digite o valor X (dividendo): "))
Y = int(input("Digite o valor Y (divisor): "))

# Operações
quociente_inteiro =  X // Y
resto_inteiro = X % Y
quociente_real = X / Y
produto = X * Y
potencia = Y ** 3

# Saída
print(quociente_inteiro)
print(resto_inteiro)
print(quociente_real)
print(produto)
print(potencia)