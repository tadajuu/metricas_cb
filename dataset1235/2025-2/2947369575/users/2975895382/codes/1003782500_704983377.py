
dividendo = int(input("Digite o valor do dividendo: "))
divisor = int(input("Digite o valor do divisor: "))

quocienteInteiro = dividendo // divisor
resto = dividendo % divisor
quocienteReal = dividendo / divisor
produto = dividendo * divisor
potencia = divisor ** 3

print(quocienteInteiro)
print(resto)
print(quocienteReal)
print(produto)
print(potencia)
