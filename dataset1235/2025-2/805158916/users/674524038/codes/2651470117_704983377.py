#Início
X = int(input("Valor de x: "))
Y = int(input("Valor de y: "))
#Quociente da divisão inteira
Q1 = X//Y
#Resto da divisão inteira
R = X%Y
#Quociente da divisão real
Q2 = X/Y
#O produto de X vezes Y
P = X*Y
#Valor de Y elevado a terceira potência
E = Y**3
#Saída
print(Q1)
print(R)
print(Q2)
print(P)
print(E)