# entrada, para que fique dois números reais diferentes 
x = int(input("digite o numero"))
y = int(input("digite um numero"))
#agora, é escrever os coisas 
Q1 = x // y # divisão inteira
R = x % y # resto da divisão inteira 
Q2 = x / y # divisão real 
Pr = x * y # multiplicação 
Pot = y ** 3 # potencia
#colocar o print para sair o resultado
print(Q1)
print(R)
print(Q2)
print(Pr)
print(Pot)