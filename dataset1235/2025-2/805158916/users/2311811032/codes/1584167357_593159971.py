# Média aritmética de números reais

# 1 Entrada

# 1.1 Primeira nota
nota_1 = float(input('1.0'))

# 1.2 Segunda nota
nota_2 = float(input('2.0'))

# 1.3 Terceira nota
nota_3 = float(input('3.0'))

# 2 Comando

# 2.1 Cálculo da média aritmética das 3 notas
media = (nota_1 + nota_2 + nota_3) / 3

# 3 Saída
print(media)