# avaliando situacoes de alunos

# 1 entrda

nota_1 = float(input('9.5'))

nota_2 = float(input('8.5'))

nota_3 = float(input('6'))

nota_4 = float(input('10'))

# 2 comando

media_ponderada = ((nota_1 * 1) + (nota_2 * 2) + (nota_3 * 3)
                   + (nota_4 * 4)) / (1 + 2 + 3 + 4)

# 3 saida

print(round(media_ponderada, 2))